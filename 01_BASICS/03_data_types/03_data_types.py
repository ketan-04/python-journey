"""
Topic: Data Types in Python
---------------------------------
Python supports different types of data for storing values.
Let's explore the most commonly used data types with examples.
"""

# String (str)
name = "Leeladhar"
print("String:", name, "| Type:", type(name))

# Integer (int)
age = 21
print("Integer:", age, "| Type:", type(age))

# Float (float)
height = 5.9
print("Float:", height, "| Type:", type(height))

# Boolean (bool)
is_student = True
print("Boolean:", is_student, "| Type:", type(is_student))

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits, "| Type:", type(fruits))

# Tuple
coordinates = (10.5, 20.8)
print("Tuple:", coordinates, "| Type:", type(coordinates))

# Dictionary
student = {"name": "Leeladhar", "age": 21, "is_student": True}
print("Dictionary:", student, "| Type:", type(student))

# Set
unique_numbers = {1, 2, 3, 3, 2}
print("Set:", unique_numbers, "| Type:", type(unique_numbers))

# NoneType
nothing = None
print("NoneType:", nothing, "| Type:", type(nothing))
